switch(day){
    case 0:
        console.log("Sunday");
        break;
    case 1:
        console.log("Monday");
        break;
    case 3:
        console.log("Tuesday");
        break;
    case 4:
        console.log("Wednesday");
        break;
    case 5:
        console.log("thrusday");
        break;
    case 6:
        console.log("saturday");
        break;
    default:
        console.log("invalid day");
        break;
}